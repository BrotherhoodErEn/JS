console.log(s1 < s2); //küçüktür
console.log(s1 <= s2); //küçük eşittir
console.log(s1 >= s1); //büyük eşittir

console.log(s1 === s2); //hem tipi eşit hem de değeri eşit
console.log(s1 !== s2); //
